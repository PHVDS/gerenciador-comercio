    private void BuscarEntreDatas() throws SQLException{
        LinkedList<Venda> vendas = new LinkedList<>();
        ResultSet r;
        TrataNumeros t = new TrataNumeros();
        Calendario c = new Calendario();
        
        Connection conexao = new Conexao().getConexao();
        if(c.retornaData(this.data1.getDate()).equals("") || c.retornaData(this.getData2().getDate()).equals(t.trataDatas(c.retornaData())))
            r = conexao.createStatement().executeQuery("SELECT * FROM vendas ORDER BY dt_com_prod desc");
        else
            r = conexao.createStatement().executeQuery("SELECT * FROM vendas WHERE dt_com_prod between ('" + t.trataDatas(c.retornaData(this.data1.getDate())) + "') and ('" + t.trataDatas(c.retornaData(this.getData2().getDate())) + "') ORDER BY dt_com_prod desc");

        while(r.next()){
            vendas.add(new Venda(r.getShort(1), r.getFloat(2), r.getFloat(7), r.getFloat(8), r.getString(4), r.getString(5)));
        }
       
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        model.setNumRows(0);
        
        for(Venda v : vendas){
            Object[] linha = {v, v.getVl_ven(), v.getVl_recebido(), v.getVl_troco(), t.tranformaDataLegivel(v.getDt_com_prod()), v.getHr_com_prod()};
            model.addRow(linha);
        }
        
    }
    
