    private void bDetalhesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bDetalhesMouseClicked
        int index = this.jTable1.getSelectedRow();
        
        if(index > -1){
            Venda venda = (Venda) this.jTable1.getValueAt(index, 0);

            Janela j = new Janela(1200, 600);
            JDetalhesCompra a = new JDetalhesCompra();
            try {
                venda.CarregarVenda();
                a.setVenda(venda);
                FormatoNumerico f = new FormatoNumerico();
                TrataNumeros t = new TrataNumeros();
                
                a.getNome1().setText("VENDA REALIZADA EM " + t.tranformaDataLegivel(a.getVenda().getDt_com_prod()) + " às " + a.getVenda().getHr_com_prod());
                a.getNome2().setText("VALOR DA VENDA: " + f.din(a.getVenda().getVl_ven()));

                this.setJ2(j);

                DefaultTableModel model = (DefaultTableModel) a.getjTable1().getModel();

                int cont = 1;
                for(Subtotal c : venda.getCompras()){
                    Object[] linha = {cont++, c.getProduto(), c.getQtd_prod(), f.din(c.getVl_prod()), f.din(c.getVl_com())}; 
                    model.addRow(linha);
                }

                j2.setVisible(true);
                j2.setResizable(false);
                j2.add(a);
                a.setJ2(j2);
                a.setJ(this.j);

                this.j.setEnabled(false);
            } catch (SQLException ex) {
                Logger.getLogger(JListarVendas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            new Mensagem().msg("Selecione uma venda para ver os detalhes!!");
        }
    }//GEN-LAST:event_bDetalhesMouseClicked

